class FranjaHoraria {

    constructor (dia, horario) {
        this.dia = dia;
        this.horario = horario;
    }

    getDia () {
        return this.dia;
    }

    setDia (dia) {
        this.dia = dia;
    }

    getHorario () {
        return this.horario;
    }

    setHorario (horario) {
        this.horario = horario;
    } 

}