class Departamento {
    
    constructor (codigo, nombre) {
        this.codigo = codigo;
        this.nombre = nombre;
    }

    getCodigo () {
        return this.codigo;
    }

    setCodigo (codigo) {
        this.codigo = codigo;
    }

    getNombre () {
        return this.nombre;
    }

    setNombre () {
        this.nombre = this.nombre;
    }

}